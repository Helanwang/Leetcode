list1 = [7, 7, 7, 8, 8, 9, 9, 9, 10]  # Output:7,7,7,8,8,9,9,9,10
for i in list1:
    print(i, end=",")  # in here i represents element/value directly from the list

for i in range(len(list1)):  # n = 9. Range is 0-9. # Output: 0,1,2,3,4,5,6,7,8
    print(i, end=",")  # in here i represents the index in the range.

